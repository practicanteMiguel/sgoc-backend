// 🔹 CONFIGURA TU DATASOURCE (ajusta si es necesario)
const AppDataSource = new DataSource({
  type: 'postgres', // o mysql
  url: process.env.DATABASE_URL, // o usa host, username, password, database
  entities: [__dirname + '/../../**/*.entity.{ts,js}'],
  synchronize: false,
});

async function run() {
  try {
    await AppDataSource.initialize();
    const repo = AppDataSource.getRepository(Employee);

    // limpiar ids (espacios raros)
    const cleanIds = idsToValidate.map(id => id.trim());

    // buscar existentes
    const existing = await repo.find({
      select: ['identification_number'],
      where: {
        identification_number: In(cleanIds),
      },
    });

    const existingSet = new Set(
      existing.map(e => e.identification_number)
    );

    // encontrar faltantes
    const missing = cleanIds.filter(id => !existingSet.has(id));

    console.log('==============================');
    console.log('📊 RESULTADO VALIDACIÓN');
    console.log('==============================');
    console.log('Total enviados:', cleanIds.length);
    console.log('Encontrados en BD:', existing.length);
    console.log('Faltantes:', missing.length);
    console.log('------------------------------');

    if (missing.length > 0) {
      console.log('🚨 IDs que faltan:');
      console.log(missing);
    } else {
      console.log('🎉 Todos existen en la BD');
    }

    await AppDataSource.destroy();
  } catch (error) {
    console.error('❌ Error:', error);
  }
}

run();